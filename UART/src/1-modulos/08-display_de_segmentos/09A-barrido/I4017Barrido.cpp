void I4017_Barrido::SetReset( void )
{
	m_pins4017[ 0 ]->ClrPin( );
	m_pins4017[ 0 ]->SetPin( );
	m_pins4017[ 0 ]->ClrPin( );
	m_indice = 0;
}

void I4017_Barrido::SetClock( void )
{
	m_pins4017[ 1 ]->ClrPin( );
	m_pins4017[ 1 ]->SetPin( );
	m_pins4017[ 1 ]->ClrPin( );
}

void I4017_Barrido::SetDigito( void )
{
	if( m_indice < m_cantidadDeDigitos )
		SetClock( );
	else
		SetReset();

	m_indice++;
}

void I4017_Barrido::SetNDigitos( uint8_t cantidadDeDigitos )
{
	m_cantidadDeDigitos = cantidadDeDigitos;
}
