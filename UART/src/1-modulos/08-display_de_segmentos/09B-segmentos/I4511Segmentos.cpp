bool I4511_Segmentos::SetSegmentos( uint8_t valor )
{
	for ( uint8_t i = 0 ; i < 4 ; i++ )
	{
		if ( ! ( valor & ( 1 << i ) ) )
			m_bcd[ i ]->ClrPin();
		else
			m_bcd[ i ]->SetPin();

	}
	m_bcd[ 4 ]->ClrPin();
	return true;
}

void I4511_Segmentos::ClrSegmentos( void )
{
	 SetSegmentos(0x0f);
}

